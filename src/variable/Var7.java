package variable;

public class Var7 {
    public static void main(String[] args) {
        int a = 1000; // 정수
        double b = 10.5; // 실수
        boolean c = true; // true, false만 입력 가능
        char d = 'A'; // 캐릭터, 문자 하나만 들어 갈 수 있음
        String e = "안녕"; // 문자열, 문자열을 다루기 위한 특별한 타입

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
